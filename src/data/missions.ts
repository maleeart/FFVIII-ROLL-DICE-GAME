// 2. ฟังก์ชันคำนวณหาไอดีฉากถัดไปตามผลทอยเต๋า D20 จริง
export const getNextNodeId = (mission: Mission, currentNodeId: string, choiceId: string, isSuccess: boolean): string | null => {
  const suffix = isSuccess ? 'success' : 'failure'
  const targetNodeId = `${currentNodeId}_${choiceId}_${suffix}`
  
  // สแกนตรวจสอบความถูกต้องว่ามีเนื้อเรื่องฉากนี้ปูทางรอไว้จริงไหม
  const nodeExists = mission.nodes.some(node => node.id === targetNodeId)
  return nodeExists ? targetNodeId : null
}